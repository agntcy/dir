        if Hardware::CPU.intel? && Hardware::CPU.is_64_bit?
            url "#{url}/dirctl-linux-amd64"
            sha256 "ef55cf632af624c03b1f53cce0a8e9f2a902253fde7f3807e4bdaf9f347a985c"

